#include<iostream>
#include "struct_array.h"
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
void print3d(ARRAY3DFLOAT& a)
{
  for(int k=a.n1b;k<=a.n1e;k++)
    for(int j=a.n2b;j<=a.n2e;j++)
    {
      for(int i=a.n3b;i<=a.n3e;i++) a.buffer[k][j][i]=k*100.0+j*10.0+i*1.0;
      for(int i=a.n3b;i<=a.n3e;i++) std::cout<<a.buffer[k][j][i]<<" ";
      std::cout<<std::endl;
    }
}
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
void print2d(ARRAY2DFLOAT& a)
{
  for(int j=a.n1b;j<=a.n1e;j++)
  {
    for(int i=a.n2b;i<=a.n2e;i++) a.buffer[j][i]=j*10.0+i*1.0;
    for(int i=a.n2b;i<=a.n2e;i++) std::cout<<a.buffer[j][i]<<" ";
    std::cout<<std::endl;
  }
}
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
void print1d(ARRAY1DFLOAT& a)
{
  for(int i=a.n1b;i<=a.n1e;i++) a.buffer[i]=i*1.0;
  for(int i=a.n1b;i<=a.n1e;i++) std::cout<<a.buffer[i]<<" ";
  std::cout<<std::endl;
}
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
int main()
{
  ARRAY3DFLOAT a3d(2,3,4);
  ARRAY2DFLOAT a2d(3,4);
  ARRAY1DFLOAT a1d(4);

  print3d(a3d);
  std::cout<<std::endl;

  print2d(a2d);
  std::cout<<std::endl;

  print1d(a1d);
  std::cout<<std::endl;

  return 0;
}
